Receipt       = require("../models/receipt");
ReceiptDetail = require("../models/receipt_detail");

module.exports.all = function (req, res) {
    return Receipt.all(function (error, receipts) {
        if (error) {
            console.log(error);
            return res.send({ 
                "error": true, 
                "msg": "Server error occured while retrieving data." 
            });
        } else {
            return res.send(receipts);
        }
    });
};

module.exports.read = function(req, res) {
    return Receipt.find(req.params.id, function (error, receipt) {
        if (error || !receipt) {
            return res.send({
                "error": true,
                "msg": "Receipt not found"
            }, 404);
        } else {
            details = ReceiptDetail
                .byReceiptId(receipt.receiptId, 
                             function (error, receiptDetails) {
                                  if (!error && receiptDetails) {
                                      receipt = JSON.stringify(receipt);
                                      receipt = JSON.parse(receipt);
                                      receipt.details = receiptDetails;
                                  }
                                  res.send(receipt);
                              });
            return receipt;
        }
    });
};


